public class TiposDeDatosPrimitivos {
    public static void main(String[] args) {
        byte elMenorByte = Byte.MIN_VALUE;
        byte elMayorByte = Byte.MAX_VALUE;

        System.out.println("El menor byte es: " + elMenorByte);
        System.out.println("El mayor byte es: " + elMayorByte);

        System.out.println("El menor short es: " + Short.MIN_VALUE);
        System.out.println("El mayor short es: " + Short.MAX_VALUE);

        System.out.println("El menor int es: " + Integer.MIN_VALUE);
        System.out.println("El mayor int es: " + Integer.MAX_VALUE);

        System.out.println("El menor long es: " + Long.MIN_VALUE);
        System.out.println("El mayor long es: " + Long.MAX_VALUE);

        // Decimales

        float variableFloat = 5.6f;
        double variableDouble = 5.6;

        char a = 'a';

        boolean truBoolean = true;
        boolean falseBoolean = false;
    }
}
